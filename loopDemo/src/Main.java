public class Main {
    public static void main(String[] args) {

        //for loop
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }
        System.out.println("for Döngüsü bitti!");

        //while loop
        int i = 0;
        while (i < 20) {
            System.out.println(i);
            i++;
        }

        System.out.println("While Döngüsü bitti!");

        //do WHile döngüsü!!

        do {
            System.out.println(i);
            i++;
        } while (i < 20);
        System.out.println("do-While Döngüsü bitti!");

    }
}